				    <h2 class="titulo">Fake</h2>
                    <a class="texto">
                    Notícias falsas podem se espalhar mais rápido do que o próprio coronavírus e ser tão perigosas quanto ele. Muitas informações imprecisas geram pânico ou o afrouxamento das medidas de prevenção e prejudicam o combate à pandemia.

                    Todos desempenhamos um papel importante em relação à nossa proteção e a do próximo. Conheça 5 fake news sobre a COVID-19 e ajude a impedir a disseminação de mitos e da doença:</a><br/>

                    <br/><a class="texto">1. Existe um medicamento específico para o tratamento ou a prevenção da COVID-19
                    Falso. Ensaios clínicos estão em andamento, mas, até o momento, não há nenhuma comprovação de que a hidroxicloroquina ou qualquer outro medicamento possa curar ou prevenir a COVID-19.</a><br/>

                    <br/><a class="texto">2. O consumo de álcool protege contra a COVID-19
                    Isso não é verdade. Além de não ter nenhuma relação com a prevenção da doença, o consumo nocivo de álcool debilita o sistema imunológico e aumenta o risco de danos à saúde.</a><br/>

                    <br/><a class="texto">3. Animais de estimação podem transmitir a COVID-19 aos humanos
                    Não há evidências sobre isso. Vários cães e gatos que estiveram em contato com humanos infectados testaram positivo para a COVID-19, mas não é possível dizer que esses animais possam transmitir a doença aos seres humanos e espalhar o vírus.</a><br/>

                    <br/><a class="texto">4. A COVID-19 só é letal em idosos
                    Falso. Idosos e pacientes com doenças crônicas correm mais risco de desenvolver formas graves da doença, mas pode haver mortalidade entre pessoas de todas as idades que contraem o vírus. No nosso centro de tratamento de COVID-19 em Aden, no Iêmen, por exemplo, as vítimas são majoritariamente homens entre 40 e 60 anos.</a><br/>

                    <br/><a class="texto">5. Só pessoas sintomáticas transmitem a COVID-19
                    Não. É importante saber que uma pessoa infectada pode levar até 14 dias para apresentar sintomas. Mas, mesmo sem os sintomas, essa pessoa pode transmitir a doença. Saiba mais sobre sintomas, prevenção e transmissão da COVID-19: <a href="https://coronavirus.msf.org.br/">https://coronavirus.msf.org.br/</a> </a>
                    
