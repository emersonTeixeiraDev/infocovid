				    <h2 class="titulo">Transmissão</h2>
                    <a class="texto">
                    De acordo com as evidências mais atuais, o SARS-CoV-2, da mesma forma que outros vírus respiratórios, é transmitido principalmente por três modos: contato, gotículas ou por aerossol.
                    <br/>A transmissão por contato é a transmissão da infecção por meio do contato direto com uma pessoa infectada (por exemplo, durante um aperto de mão seguido do toque nos olhos, nariz ou boca), ou com objetos e superfícies contaminados (fômites).<br/>
                    <br/>A transmissão por gotículas é a transmissão da infecção por meio da exposição a gotículas respiratórias expelidas, contendo vírus, por uma pessoa infectada quando ela tosse ou espirra, principalmente quando ela se encontra a menos de 1 metro de distância da outra.<br/>
                    <br/>A transmissão por aerossol é a transmissão da infecção por meio de gotículas respiratórias menores (aerossóis) contendo vírus e que podem permanecer suspensas no ar, serem levadas por distâncias maiores que 1 metro e por períodos mais longos (geralmente horas).<br/>
                    <br/> A epidemiologia do SARS-CoV-2 indica que a maioria das infecções se espalha por contato próximo (menos de 1 metro), principalmente por meio de gotículas respiratórias. Não há evidência de transmissão eficiente para pessoas em distâncias maiores ou que entram em um espaço horas depois que uma pessoa infectada esteve lá.
